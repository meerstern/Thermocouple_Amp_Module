void GetTmp(float *tmp, uint8_t *err)
{
	uint8_t 	rdata[4]={0x00};
	uint8_t 	wdata[4]={0x00};

	uint32_t    rawData;
	int32_t 	valData;
	float 		valFloat;


	//CS Low
	LL_GPIO_ResetOutputPin(SPI1_CS_GPIO_Port, SPI1_CS_Pin);
	//Recieve
	HAL_SPI_TransmitReceive(&hspi1, wdata, rdata, 4, 0xFFF);
	//CS High
	LL_GPIO_SetOutputPin(SPI1_CS_GPIO_Port, SPI1_CS_Pin);

	rawData = rdata[0];
	rawData <<= 8;
	rawData |= rdata[1];
	rawData <<= 8;
	rawData |= rdata[2];
	rawData <<= 8;
	rawData |= rdata[3];
	valData=rawData;

	float 		internal;
	internal = (valData >> 4) & 0x7FF;
	internal *= 0.0625;
	if ((valData >> 4) & 0x800)
		internal *= -1;

	if (valData & 0x80000000)
		valData = 0xFFFFC000 | ((valData >> 18) & 0x00003FFF);
	else
		valData >>= 18;
	valFloat=valData;
	valFloat *= 0.25;

	//printf("DATA:%2X %2X %2X %2X \t",rdata[0],rdata[1],rdata[2],rdata[3]);

	*tmp=valFloat;
	*err=rdata[3]&0x03;

}

void ConvertTemp2Duty(float temp, uint8_t err, uint16_t *duty)
{
	float volt=0.0f;
	if(HighGainMode==true)
		volt= temp/HGAIN + VOFFSET+(AMP_OFFSET);
	else
		volt= temp/LGAIN + VOFFSET+(AMP_OFFSET);

	float dutyValue=0;
	if(volt<0)
		volt=0.0f;

	dutyValue = volt/MAXVOLT*MAXDUTY;

	if(dutyValue>4095.0f)
		dutyValue=MAXDUTY;
	*duty=(uint16_t)dutyValue;

#ifdef USE_DEBUG
	//printf("duty:%d, v:%f, err:%d,",*duty, volt-(AMP_OFFSET), err);
#endif

}
